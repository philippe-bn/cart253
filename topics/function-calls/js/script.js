"use strict";

/**
 * Create a canvas to draw on.
*/
function setup() {
    createCanvas(900, 500);
}


/**
 * Draw the sea and boat.
*/
function draw() {
    background(50, 50, 255);
    rect(100, 100, 30, 70);
}